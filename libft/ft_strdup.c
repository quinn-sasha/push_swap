#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*new_string;
	int		i;

	new_string = malloc(ft_strlen(s) + 1);
	if (!new_string)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new_string[i] = s[i];
		i++;
	}
	new_string[i] = '\0';
	return (new_string);
}
